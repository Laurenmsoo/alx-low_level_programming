/**
 * add - adds 2 numbers
 * @a: first number
 * @b: second number
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substracts 2 numbers
 * @a: first number
 * @b: second number
 * Return: result of sub
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides 2 numbers
 * @a: first number
 * @b: second number
 * Return: result of multiplication
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - finds the remainder of two numbers
 * @a: first number
 * @b: second number
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
